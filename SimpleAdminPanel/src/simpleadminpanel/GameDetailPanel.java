    void UpdateDetails()
    {
        String name = jTextField1.getText();            
        String genre = jTextField3.getText();;           
        String detail = jTextArea1.getText();           
        String ver = jTextField2.getText();

        try{
            
            sql = "UPDATE gamedes SET name='"+name+"', genre='"+genre+"', detail='"+detail+"', version='"+ver+"'";
            stat = (Statement) connection.prepareStatement(sql);
            stat.execute(sql);
            
            JOptionPane.showMessageDialog(null, "Berhasil di Update");
            GameDetails();
        }catch(HeadlessException | SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
