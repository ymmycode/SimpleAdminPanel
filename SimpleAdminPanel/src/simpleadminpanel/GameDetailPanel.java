    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try {
            // TODO add your handling code here:
            //quit the launcher
            
            int response = JOptionPane.showConfirmDialog(this, "Apakah Anda Yakin Ingin ke Menu Utama?", "Confirm", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            
            switch (response) {
                case JOptionPane.YES_OPTION:
                    Dashboard dsb = new Dashboard();
                    dsb.setVisible(true);
                    this.dispose();
                    break;
                case JOptionPane.NO_OPTION:
                    return;
                case JOptionPane.CLOSED_OPTION:
                    break;
                default:
                    break;
            }
        } catch (SQLException ex) {
            Logger.getLogger(GameDetailPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

